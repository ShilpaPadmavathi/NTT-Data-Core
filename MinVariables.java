package GradleDem;

public class MinVariables {
	
	public static void main(String[] args) {
		int intValue=Integer.MIN_VALUE;
		float floatValue=Float.MIN_VALUE;
		byte byteValue=Byte.MIN_VALUE;
		long longValue=Long.MIN_VALUE;
		double doubleValue=Double.MIN_VALUE;
		boolean booleanValue=Boolean.TRUE;
		
		System.out.println("Smallest Integer Value "+intValue);
		System.out.println("Smallest Float Value "+floatValue);
		System.out.println("Smallest Byte Value "+byteValue);
		System.out.println("Smallest Long Value "+longValue);
		System.out.println("Smallest Double Value "+doubleValue);
		System.out.println("Boolean Value "+booleanValue);
	}
	
	

}
